# exploring range()
for number in range(1,11):
    print(number)
# +1 is default step that's why it will not print.
for i in range(11,1):
    print(i)

# printing (11,0) by using step
for i in range(11,0,-1):
    print(i)

for n in range(1,51,2):
    print(n)

for n in range(200,1,-10):
    print(n)